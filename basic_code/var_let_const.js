// 'const' is used for constant values that should not be reassigned.
// Data type: Number
const accountNo = 456456;  

// 'let' is used for variables that can be reassigned later if needed.
// Data type: String
let accountId = "Harsh456idbi";  

// 'var' is the old way to declare variables (function-scoped, not block-scoped). 
// Generally not recommended today, but still works.
// Data type: String
var accountHolder = "Harsh Chauhan";  

// console.table() prints data in a tabular form in the console.
console.table([accountNo, accountId, accountHolder]);